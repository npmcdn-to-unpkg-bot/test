
/*
Type casting


For generic type-casting
---------------------------
C++ is a strong-typed language. Many conversions, specially those that imply a different interpretation of the value, r
equire an explicit conversion, known in C++ as type-casting. 
There exist two main syntaxes for generic type-casting: 
    functional and c-like:

*/

double x = 10.3;
int y;
y = int (x);    // functional notation
y = (int) x;    // c-like cast notation 


